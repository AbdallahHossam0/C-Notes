// * the Model View Controller (MVC) Paradigm

// a set of data is called the model
// A view is a particular visualization of the model
// the controller is the piece of code that changes the model in response to
// some event

// the data members are the model
// the getter are used by the view to show the data
// the controller controls the data by the setters

